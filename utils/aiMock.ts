function pickRandom(list: string[]): string {
  const index = Math.floor(Math.random() * list.length);
  return list[index];
}

export async function generateAffirmation(mood: Mood): Promise<string> {
  // Имитация запроса к LLM: небольшая задержка и "умный" текст
  await new Promise((resolve) => setTimeout(resolve, 600));

  if (mood === 'calm') {
    return pickRandom(CALM_VARIANTS);
  }

  if (mood === 'neutral') {
    return pickRandom(NEUTRAL_VARIANTS);
  }

  return pickRandom(STRESSED_VARIANTS);
}

